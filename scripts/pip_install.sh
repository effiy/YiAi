# 安装爬虫库，用于网页抓取功能
pip install -U crawl4ai

# 安装playwright及其依赖，为爬虫提供浏览器环境
python -m playwright install --with-deps chromium

# 安装FastAPI和uvicorn，用于创建和运行Web服务器
pip install -U fastapi uvicorn

# 安装MongoDB驱动
pip install -U motor

# 安装PyMongo，用于与MongoDB交互
pip install -U pymongo

# 安装asyncio，用于异步编程
pip install -U asyncio

# 安装aiofiles，用于异步文件操作
pip install -U aiofiles

# 安装python-dotenv，用于读取环境变量
pip install -U python-dotenv

# 安装python-decouple，用于读取环境变量
pip install -U python-decouple
